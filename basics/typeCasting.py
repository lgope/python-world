# int()
x = int(1)  # x will be 1
y = int(4.8)  # y will be 4
z = int("6")  # z will be 6

a = str(100)
b = int(a)
c = type(b)

print(c)
print(x, y, z)

# float()
x = float(4)  # x will be 4.0
y = float(4.8)  # y will be 4.8
z = float("8")  # z will be 8.0
w = float("9.2")  # w will be 9.2

print(x, y, z, w)

# str()
x = str("s4")  # x will be 's4'
y = str(4)  # y will be '4'
z = str(9.0)  # z will be '9.0'

print(x, y, z)
